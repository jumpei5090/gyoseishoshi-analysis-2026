def main():
    from models import KeywordMapping, Topic
    db = SessionLocal()
    try:
        # Get existing keywords to avoid duplicates
        existing = {(km.keyword, km.topic_id) for km in db.query(KeywordMapping).all()}
        # Verify all topic IDs exist
        valid_ids = {t.id for t in db.query(Topic).all()}
        
        count = 0
        skipped = 0
        invalid = 0
        for keyword, topic_id in KEYWORD_MAPPINGS.items():
            if topic_id not in valid_ids:
                print(f"  INVALID topic_id {topic_id} for keyword '{keyword}'")
                invalid += 1
                continue
            if (keyword, topic_id) in existing:
                skipped += 1
                continue
            km = KeywordMapping(keyword=keyword, topic_id=topic_id)
            db.add(km)
            count += 1
        
        db.commit()
        print(f"\n✅ 完了: {count} キーワードを追加, {skipped} 件スキップ（既存）, {invalid} 件無効")
    except Exception as e:
        db.rollback()
        print(f"❌ エラー: {e}")
        raise
    finally:
        db.close()

if __name__ == "__main__":
    main()
