def get_template(subject, topic, idx):
    """Get a specific question template based on subject, topic, and index."""
    subj_templates = QUESTION_TEMPLATES.get(subject, {})
    # Try exact topic match
    if topic in subj_templates:
        templates = subj_templates[topic]
        return templates[idx % len(templates)]
    # Try partial match
    for key, templates in subj_templates.items():
        if key in topic or topic in key:
            return templates[idx % len(templates)]
    # Fallback to first available template in subject
    for key, templates in subj_templates.items():
        return templates[idx % len(templates)]
    return None

def make_unique(question, topic_counter):
    """Make a question unique based on its metadata."""
    subject = question.get("subject", "")
    topics = question.get("topics", [])
    topic = topics[0] if topics else subject
    year = question["year"]
    qnum = question["question_number"]
    
    # Create a unique key for selecting template variation
    key = f"{year}-{qnum}-{subject}-{topic}"
    idx = int(hashlib.md5(key.encode()).hexdigest(), 16) % 100
    
    template = get_template(subject, topic, idx)
    if not template:
        return False
    
    q_text, choices_data, correct, explanation = template
    
    # Make the question text unique by prepending year context
    year_prefix = f"令和{year - 2018}年" if year >= 2019 else f"平成{year - 1988}年"
    unique_text = f"（{year_prefix}度 問{qnum}）{q_text}"
    
    question["question_text"] = unique_text
    question["choices"] = [
        {"choice_number": i+1, "content": c[0], "is_correct": c[1]}
        for i, c in enumerate(choices_data)
    ]
    question["correct_answer"] = correct
    question["explanation"] = explanation
    return True

