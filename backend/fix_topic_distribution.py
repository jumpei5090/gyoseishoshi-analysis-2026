def main():
    with open(DATA_PATH, "r") as f:
        questions = json.load(f)
    
    updated = 0
    for q in questions:
        key = (q["year"], q["question_number"])
        if key in TOPIC_MAP:
            old = q.get("topics", [])
            new = TOPIC_MAP[key]
            if old != new:
                q["topics"] = new
                updated += 1
    
    with open(DATA_PATH, "w") as f:
        json.dump(questions, f, ensure_ascii=False, indent=2)
    
    print(f"Updated {updated} question topic assignments")
    
    # Verify distribution
    print("\n=== Updated Topic Variation Check ===")
    for topic_name in ["政治", "情報通信", "文章理解", "取消訴訟", "行政行為"]:
        counts = {}
        for q in questions:
            for t in q.get("topics", []):
                if t == topic_name:
                    counts[q["year"]] = counts.get(q["year"], 0) + 1
        yearly = [counts.get(y, 0) for y in range(2015, 2025)]
        print(f"  {topic_name:12s}: {yearly}  (unique={len(set(yearly))} values)")

if __name__ == "__main__":
    main()
