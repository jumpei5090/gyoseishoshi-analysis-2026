def main():
    """Generate complete 600-question dataset."""
    # Load existing questions to preserve real data that was already entered
    path = os.path.join(os.path.dirname(os.path.abspath(__file__)), "data", "seed_questions.json")
    with open(path, "r", encoding="utf-8") as f:
        existing = json.load(f)
    
    # Index existing questions by (year, question_number)
    existing_map = {}
    for q in existing:
        key = (q["year"], q["question_number"])
        existing_map[key] = q
    
    # Generate all 600 questions
    all_questions = []
    for year in range(2024, 2014, -1):
        for qnum in range(1, 61):
            key = (year, qnum)
            subject, law, fmt, topics = get_question_meta(qnum)
            correct = str(OFFICIAL_ANSWERS.get(year, {}).get(qnum, 0))
            
            if key in existing_map:
                # Keep existing real data but update metadata
                q = existing_map[key].copy()
                q["subject"] = subject
                q["law"] = law
                q["format"] = fmt
                if not q.get("topics"):
                    q["topics"] = topics
            else:
                # Create new placeholder question
                q = {
                    "year": year,
                    "question_number": qnum,
                    "subject": subject,
                    "law": law,
                    "format": fmt,
                    "topics": topics,
                    "question_text": "",
                    "choices": [],
                    "correct_answer": correct if correct != "0" else "",
                    "explanation": "",
                }
            
            all_questions.append(q)
    
    # Save
    with open(path, "w", encoding="utf-8") as f:
        json.dump(all_questions, f, ensure_ascii=False, indent=4)
    
    # Stats
    total = len(all_questions)
    has_text = sum(1 for q in all_questions if q.get("question_text", "").strip())
    empty = total - has_text
    
    print(f"✅ Generated {total} questions total")
    print(f"   With content: {has_text}")
    print(f"   Need content: {empty}")
    
    # Show per-year stats
    for year in range(2024, 2014, -1):
        year_qs = [q for q in all_questions if q["year"] == year]
        year_with_text = sum(1 for q in year_qs if q.get("question_text", "").strip())
        print(f"   {year}: {year_with_text}/60 have content")

if __name__ == "__main__":
    main()
