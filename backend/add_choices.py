# Choice templates by topic — each returns (choices_list, correct_number, explanation)
def make_choices(q):
    topic = q["topics"][0] if q["topics"] else ""
    law = q.get("law", "")
    fmt = q.get("format", "5肢択一")
    year = q["year"]
    qnum = q["question_number"]

    # 記述式 doesn't have multiple choices
    if fmt == "記述":
        return _kijutsu_answer(topic, law)

    # 多肢選択 has fill-in-the-blank style
    if fmt == "多肢選択":
        return _tashisentaku_answer(topic, law)

    # Default: 5肢択一
    return _gotakuitsu_answer(topic, law)


def _gotakuitsu_answer(topic, law):
    """Generate 5 choices for 5肢択一 questions."""
    templates = CHOICE_BANK.get(topic)
    if not templates:
        templates = _generic_choices(topic, law)

    correct = random.randint(1, 5)
    explanation = templates["explanation"]

    choices = []
    for i in range(1, 6):
        if i == correct:
            choices.append({
                "choice_number": i,
                "content": templates["correct"],
                "is_correct": True
            })
        else:
            wrong_idx = (i - 1) % len(templates["wrong"])
            choices.append({
                "choice_number": i,
                "content": templates["wrong"][wrong_idx],
                "is_correct": False
            })

    return choices, str(correct), explanation


def _kijutsu_answer(topic, law):
    """記述式 — model answer."""
    answers = KIJUTSU_BANK.get(topic, {
        "answer": f"{topic}に関する正解の記述例です。",
        "explanation": f"{topic}についての解説です。"
    })
    return [], answers["answer"], answers["explanation"]


def _tashisentaku_answer(topic, law):
    """多肢選択 — 4 blanks with word bank."""
    answers = TASHISENTAKU_BANK.get(topic, {
        "blanks": ["ア: 適法性", "イ: 合理性", "ウ: 必要性", "エ: 相当性"],
        "explanation": f"{topic}に関する多肢選択の解説です。"
    })
    choices = []
    for i, blank in enumerate(answers["blanks"]):
        choices.append({
            "choice_number": i + 1,
            "content": blank,
            "is_correct": True
        })
    return choices, "ア〜エの組合せ", answers["explanation"]


