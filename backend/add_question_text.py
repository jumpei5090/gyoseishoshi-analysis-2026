TASHJISEN_TEMPLATE = "次の文章の空欄〔 ア 〕～〔 エ 〕に当てはまる語句を、枠内の選択肢から選びなさい。"


def generate_question_text(q):
    """Generate representative question text."""
    fmt = q.get("format", "5肢択一")
    topics = q.get("topics", [])
    year = q.get("year", 2024)
    
    if not topics:
        return None
    
    topic = topics[0]  # Primary topic
    
    if fmt == "記述":
        return KIJUTSU_TEMPLATES.get(topic, f"{topic}について、40字程度で記述しなさい。")
    elif fmt == "多肢選択":
        base = QUESTION_TEMPLATES.get(topic, f"{topic}に関する次の記述のうち、妥当なものはどれか。")
        return TASHJISEN_TEMPLATE + "\n\n" + base.replace("次の記述のうち、", "").replace("正しいものはどれか。", "").replace("妥当なものはどれか。", "")
    else:
        return QUESTION_TEMPLATES.get(topic, f"{topic}に関する次の記述のうち、妥当なものはどれか。")


def main():
    data_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)), "data")
    path = os.path.join(data_dir, "seed_questions.json")
    
    with open(path, "r", encoding="utf-8") as f:
        questions = json.load(f)
    
    updated = 0
    for q in questions:
        if not q.get("question_text"):
            text = generate_question_text(q)
            if text:
                q["question_text"] = text
                updated += 1
    
    with open(path, "w", encoding="utf-8") as f:
        json.dump(questions, f, ensure_ascii=False, indent=4)
    
    print(f"✅ {updated} questions updated with question_text (total: {len(questions)})")


if __name__ == "__main__":
    main()
