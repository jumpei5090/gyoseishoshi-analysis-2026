def year_label(year):
    if year >= 2019: return f"令和{year-2018}年"
    return f"平成{year-1988}年"

def main():
    path = os.path.join(os.path.dirname(os.path.abspath(__file__)), "data", "seed_questions.json")
    with open(path, "r", encoding="utf-8") as f:
        existing = json.load(f)
    
    # Index existing questions
    existing_map = {}
    for q in existing:
        key = (q["year"], q["question_number"])
        existing_map[key] = q
    
    all_questions = []
    for year in range(2024, 2014, -1):
        for qnum in range(1, 61):
            key = (year, qnum)
            subject, law, fmt, topics = get_meta(qnum)
            ans = ANSWERS.get(year, {}).get(qnum, 0)
            correct = str(ans) if ans != 0 else "全員正解"
            
            if key in existing_map and existing_map[key].get("question_text","").strip():
                # Preserve existing data with correct answer
                q = existing_map[key].copy()
                q["subject"] = subject
                q["law"] = law
                q["format"] = fmt
                if not q.get("topics"): q["topics"] = topics
                if ans != 0: q["correct_answer"] = correct
            else:
                # Create question with correct answer
                q = {
                    "year": year,
                    "question_number": qnum,
                    "subject": subject,
                    "law": law,
                    "format": fmt,
                    "topics": topics,
                    "question_text": "",
                    "choices": [],
                    "correct_answer": correct,
                    "explanation": "",
                }
            all_questions.append(q)
    
    with open(path, "w", encoding="utf-8") as f:
        json.dump(all_questions, f, ensure_ascii=False, indent=4)
    
    # Stats
    total = len(all_questions)
    has_text = sum(1 for q in all_questions if q.get("question_text","").strip())
    print(f"✅ {total} questions generated")
    print(f"   With content: {has_text}")
    print(f"   Need content: {total - has_text}")
    for y in range(2024, 2014, -1):
        yqs = [q for q in all_questions if q["year"]==y]
        yt = sum(1 for q in yqs if q.get("question_text","").strip())
        print(f"   {y}: {yt}/60")

if __name__ == "__main__":
    main()
